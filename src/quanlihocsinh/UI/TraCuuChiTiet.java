    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        String studentId = student.getID();
        int reply = JOptionPane.showConfirmDialog(
                this,
                "Bạn có muốn xóa học sinh " + studentId + " ra khỏi hệ thống?",
                "Xóa học sinh",
                JOptionPane.YES_NO_OPTION
        );
        if(reply == JOptionPane.YES_OPTION) {
            try {
                Connection connection = Main.sqlConnection.getConnection();
                Statement statement = connection.createStatement();
                
                // Xoa bang diem hoc sinh
                statement.executeUpdate("DELETE FROM BANGDIEMMON WHERE MaHocSinh='" + studentId + "'");
                // Xoa hoc sinh
                statement.executeUpdate("DELETE FROM HOCSINH WHERE MaHocSinh='" + studentId + "'");
                connection.close();
                
                TraCuuHocSinh.deleteSelectedStudent();
                JOptionPane.showMessageDialog(this, "Đã xóa thành công học sinh " + studentId);
                setVisible(false);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(TraCuuChiTiet.class.getName()).log(Level.SEVERE, null, ex);
                Main.sqlConnection.closeAllConnection();
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

