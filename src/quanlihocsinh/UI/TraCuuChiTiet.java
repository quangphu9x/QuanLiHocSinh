    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        if(!checkEmptyField()) // kiem tra xem thong tin da duoc dien day du chua
            return;
        
        int d = d_birthdayComboBox.getSelectedIndex() + 1;
        int m = m_birthdayComboBox.getSelectedIndex() + 1;
        int y = Integer.parseInt(y_birthdayField.getText());
        if(!CheckDate.checkValidDate(d, m, y)) {
            JOptionPane.showMessageDialog(this, "Ngày tháng không hợp lệ!");
            return;
        }
        
        try {
            Connection connection = Main.sqlConnection.getConnection();
            Statement statement = connection.createStatement();
            
            // kiem tra gioi han ve tuoi
            String[] checkBirthDayLim = CheckDate.checkBirthdayLimit(y, statement).split(",");
            if(checkBirthDayLim[2].equals("false")) {
                JOptionPane.showMessageDialog(
                        this,
                        "Tuổi học sinh phải từ " + checkBirthDayLim[0] + " đến " + checkBirthDayLim[1]
                );
                connection.close();
                return;
            }
            
            // lay cac thong tin hoc sinh
            String id = idField.getText();
            String name = nameField.getText();
            int sex = sexField.getSelectedIndex();
            String address = addressField.getText();
            String email = emailField.getText();
            
            // tao chuoi ngay sinh
            Date date = new GregorianCalendar(y, m - 1, d).getTime();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String birthday = df.format(date);
            
            statement.executeUpdate("UPDATE HOCSINH SET"
                    + " HoVaTen='" + name + "', GioiTinh=" + sex + ", DiaChi='"
                    + address + "', Email='" + email + "', NgaySinh='"
                    + birthday + "' WHERE MaHocSinh='" + id + "'"
            );
            
            connection.close();
            student.setName(name);
            student.setSex(sex);
            student.setAddress(address);
            student.setEmail(email);
            student.setBirthday(birthday);
            TraCuuHocSinh.reloadTable();
            JOptionPane.showMessageDialog(this, "Lưu thông tin thành công!");
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(TraCuuChiTiet.class.getName()).log(Level.SEVERE, null, ex);
            Main.sqlConnection.closeAllConnection();
        }
        
        
    }//GEN-LAST:event_saveButtonActionPerformed

    private void initData() {
        // hien thi danh sach ngay, thang
        for(int i = 0; i < 31; i++)
            d_birthdayComboBox.addItem(Integer.toString(i + 1));
        for(int i = 0; i < 12; i++)
            m_birthdayComboBox.addItem(Integer.toString(i + 1));
    }
    
    public void setStudent(Student student) {
        this.student = student;
        loadStudentInfo();
    }
    
    private void loadStudentInfo() {
        idField.setText(student.getID());
        nameField.setText(student.getName());
        gradeField.setText(Integer.toString(student.getGrade()));
        classField.setText(student.getClassName());
        sexField.setSelectedIndex(student.getSex());
        
        String[] birthday = student.getBirthday().split("-"); // dinh dang yyyy-MM-dd
        d_birthdayComboBox.setSelectedIndex(
                Integer.parseInt(birthday[2]) - 1);
        m_birthdayComboBox.setSelectedIndex(
                Integer.parseInt(birthday[1]) - 1);
        y_birthdayField.setText(birthday[0]);
        
        addressField.setText(student.getAddress());
        emailField.setText(student.getEmail());
        avgT1Field.setText(Float.toString(student.getAVGT1()));
        avgT2Field.setText(Float.toString(student.getAVGT2()));
    }
    
